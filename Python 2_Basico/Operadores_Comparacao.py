#Operadores de Comparacao
#Quando este tipo de operador é usado, o resultado é um booleano

#Igualdade "=="
#Qdo a condicao for atendida, responde true
#Qdo a condicao NAO for atendida, responde false
saldo=450
saque=200
print(saldo==saque)

#Diferenca "!="
#Qdo a condicao for atendida, responde false
#Qdo a condicao NAO for atendida, responde true
saldo=450
saque=200
print(saldo!=saque)

#Maior ">", e Maior ou Igual ">="
saldo=450
saque=200
print(saldo>saque)

saldo=450
saque=200
print(saldo>=saque)

#Menor "<", e Menor ou Igual "<="
saldo=450
saque=200
print(saldo<saque)

saldo=450
saque=200
print(saldo<=saque)
print()

