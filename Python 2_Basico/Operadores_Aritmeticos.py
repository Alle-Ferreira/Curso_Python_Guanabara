#Adicao
print(1+3)

#Subtracao
print(9-4)

#Multiplicacao
print(4*3)

#Divisao
print(9/2)

#Divisao Inteira
print(9//2)

#Modulo (resto da divisao)
print(9%2)

#Exponenciacao
print(3**2)

#Precedencia: divisao e multiplicacao precedem adicao e subtracao
print(10-(5*2))
print((10-5)*2)
print(10-5*2)

#Precedencia: exponenciacao precede divisao e multiplicacao
print(10**2*2)
print(10**(2*2))

#Precedencias iguais: executa primeiro o que estiver mais a esquerda
print(10/2*4)
