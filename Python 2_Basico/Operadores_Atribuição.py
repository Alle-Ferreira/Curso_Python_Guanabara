#Operadores de Atribuição "="

#Atribuicao Simples "="
saldo=500
print(saldo)

#Atribuicao com Adicao "+="
saldo=500
saldo+=200
print(saldo)

#Atribuicao com Subtracao "-="
saldo=500
saldo-=100
print(saldo)

#Atribuicao com Multiplicacao "*="
saldo=500
saldo*=2
print(saldo)

#Atribuicao com Divisao "/="
saldo=500
saldo/=5
print(saldo)

saldo=500
saldo//=5
print(saldo)

#Atribuicao com Módulo (restante) "%="
saldo=500
saldo%=480
print(saldo)

#Atribuicao com Exponenciacao "**="
saldo=80
saldo**=2
print(saldo)
print()

